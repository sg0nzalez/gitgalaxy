def _validate_offline_key(license_key: str) -> str:
    """
    Mathematically verifies an offline license key using pure Python RSA.
    Returns specific states: "VALID", "EXPIRED", "MISSING", or "INVALID".
    """
    if not license_key:
        return "MISSING"

    try:
        parts = license_key.split("-")

        # Key must contain exactly 5 segments: GG - TIER - CUSTOMER - EXPDATE - SIGNATURE
        if len(parts) != 5 or parts[0] != "GG":
            return "INVALID"

        tier, customer, exp_date_str, signature_hex = (
            parts[1],
            parts[2],
            parts[3],
            parts[4],
        )

        # 1. RECONSTRUCT PAYLOAD & HASH
        # We recreate the exact string the minter signed, then hash it.
        payload = f"{tier}-{customer}-{exp_date_str}".encode("utf-8")
        payload_hash = hashlib.sha256(payload).digest()

        # Convert the raw bytes of the SHA-256 hash into a Python integer
        hash_int = int.from_bytes(payload_hash, byteorder="big")

        # 2. PURE PYTHON RSA VERIFICATION (Authenticate BEFORE checking dates)
        try:
            signature_int = int(signature_hex, 16)
            # RSA Math: (Signature ^ E) mod N
            decrypted_hash_int = pow(signature_int, RSA_E, RSA_N)
        except ValueError:
            return "INVALID"

        # If the math fails, the key is forged or tampered with.
        if hash_int != decrypted_hash_int:
            return "INVALID"

        # 3. VERIFY EXPIRATION DATE (Only reached if the key is cryptographically authentic)
        try:
            exp_date = datetime.datetime.strptime(exp_date_str, "%Y%m%d")
            if datetime.datetime.now() > exp_date:
                return "EXPIRED"
        except ValueError:
            return "INVALID"

        return "VALID"

    except Exception:
        return "INVALID"


